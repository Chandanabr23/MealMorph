/// Spacing scale for the Digital Greenhouse system.
///
/// Values are in logical pixels, anchored to a 1rem = 16px base.
/// Use these instead of ad-hoc magic numbers so spacing stays consistent
/// with the "breathing" editorial rhythm the design system prescribes.
abstract final class AppSpacing {
  static const double xs = 4;
  static const double sm = 8;
  static const double md = 16;
  static const double lg = 32;
  static const double xl = 48;
  static const double xxl = 64;
}
